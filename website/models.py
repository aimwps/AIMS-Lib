from django.db import models

# Create your models here.
class ModuleNavData(models.Model):
    MODULES = (("pathways", "pathways"),
                ("community", "community"),
                ("aims", "aims"),
                ("library", "library"))
    nav_title = models.TextField()
    nav_info = models.TextField()
    nav_module = models.CharField(max_length=100, choices=MODULES)
    module_sub_page = models.CharField(max_length=255)
